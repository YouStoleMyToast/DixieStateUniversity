<h1>Ranks</h1>
<br>
<h2>Pirate Ranks</h2>
<h3>Swabbie</h3>
<p>The new member rank. Swabbie is considered a full-membership rank. New Swabbies are assigned mentors to help them learn what they will need to be successful Pirates.</p>
<h3>Privateer</h3>
<p>After approximately one month in OIP, Swabbies are eligible for promotion to Privateers. Requirements for promotion include: active posting on the forums and participation in IRC and regular attendance at events.</p>
<h3>Buccaneer</h3>
<p>After approximately two months in OIP, Privateers are eligible for promotion to Buccaneers. Requirements for promotion include: active posting on the forums and participation in IRC and regular attendance at events. Buccaneers are eligible to serve as mentors to new members, and also to serve as half-ops on #OIP IRC.</p>
<h3>Swashbuckler</h3>
<p>Swashbuckler is the senior member rank in OIP. After approximately four months in OIP, Buccaneers are eligible for promotion to Swashbucklers. Requirements for promotion include: active posting on the forums and participation in IRC and regular attendance at events or hosting events. Recruitment and mentorship are strongly recommended for promotion.</p>
<h3>Council</h3>
<p>OIP is governed by four council members. Council is an appointed rank. Council make the day-to-day decisions for OIP in consultation with the senior members and former council members.</p>
